<strong>Cycle</strong>
<p>Cycle indicates how often the security check will change to the next group/user. They include monthly, weekly and daily and will change at the begining of each (with weekly starting on Sunday).</p>

<strong>Changes each cycle</strong>
<p>They will either be group (multiple users per entry) or individual (single user per entry).</p>
<p>Note: Once set, the type (groups/individuals) cannot be changed later. You will be forced to delete and recreate the security check.</p>